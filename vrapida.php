<script>
	$(function(){
		checkViewport();
		//Event handler for window resize
		$(window).resize(function(){
			checkViewport();
		});
	});
	function hideCol(index){
		if($('table.quickview th').eq(index).is(':hidden'))
			return true;
		$("table.quickview th").eq(index).hide();
		$("table.quickview tr").each(function(){
			$(this).find('td').eq(index).hide();
		});
	}
	function showCol(index){
		if(!$('.quickview th').eq(index).is(':hidden'))
			return true;
		$('table.quickview th').eq(index).show();
		$("table.quickview tr").each(function(){
			$(this).find('td').eq(index).show();
		});
	}
	function showAllCols(){
		$('table.quickview th, table.quickview tr td').show();
	}
	function checkViewport(){
		var width = $(window).width();
		var col = [];
		if(width>=965){showAllCols();return true;}
		if(width<965){col.push($('.quickview th.frente').index());}
		if(width<910){col.push($('.quickview th.ptas').index());}
		if(width<820){col.push($('.quickview th.recamaras').index()); 
			col.push($('.quickview th.bathrooms').index());}
		if(width<768){col.push($('.quickview th.const').index());}
		if(width<660){col.push($('.quickview th.terreno').index());}
		for (var i = 0; i < col.length; i++)
			hideCol(col[i]);
		var in_array = false;
		for(var i=0;i<$('.quickview th').length;i++){
			for (var j = 0; j < col.length; j++) {
				if(col[j]==i)
					in_array = true;
			}
			if(!in_array)
				showCol(i);
		}
	}
</script>
